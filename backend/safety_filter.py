# ★ 年龄提取与判断
# ============================================================
def _check_age_issue(text: str) -> tuple[bool, str]:
    """
    从用户输入中提取年龄信息，判断是否属于特殊人群。

    返回: (是否触发, 匹配到的描述)
    - 年龄 >= 60: 老年人，需要医学评估
    - 年龄 <= 17: 未成年人，需要监护人指导
    """
    # 匹配 "我XX岁" / "今年XX岁" / "XX岁了" 等模式
    age_patterns = [
        r'(\d{1,3})\s*岁',
    ]

    for pattern in age_patterns:
        matches = re.findall(pattern, text)
        for age_str in matches:
            try:
                age = int(age_str)
                if age >= 60:
                    return True, f"60岁以上（{age}岁）"
                if age <= 17:
                    return True, f"未成年人（{age}岁）"
            except ValueError:
                continue

    # 检查文字描述
    age_text_patterns = [
        ("六十岁以上", "60岁以上老人"),
        ("六十五岁", "老年人"),
        ("七十岁", "老年人"),
        ("七十五岁", "老年人"),
        ("八十岁", "老年人"),
    ]
    for pattern, desc in age_text_patterns:
        if pattern in text:
            return True, desc

    return False, ""


