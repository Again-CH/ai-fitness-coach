# ★ 核心检测函数
# ============================================================
def check_user_input(user_input: str) -> dict:
    """
    检测用户输入是否包含敏感内容，返回安全等级和相应处理信息。

    这是安全熔断机制的核心函数，在 Safety_Gate 节点中调用。

    检测逻辑（按优先级）：
        1. 疾病关键词 → BLOCK
        2. 严重症状关键词 → BLOCK
        3. 特殊人群关键词 → BLOCK
        4. 年龄判断（>=60 或 <=17）→ BLOCK
        5. 灰色地带关键词 → WARN
        6. 以上都不匹配 → SAFE

    Args:
        user_input: 用户输入的原始文本

    Returns:
        dict，格式如下：
        - 硬熔断: {"level": "block", "trigger": "疾病/症状/特殊人群", "keyword": "高血压", "message": "⚠️ 安全提示..."}
        - 灰色地带: {"level": "warn", "keyword": "腰酸", "warning": "*温馨提示：腰部酸痛可能涉及腰椎问题...*"}
        - 安全: {"level": "safe"}
    """
    if not user_input or not user_input.strip():
        return {"level": "safe"}

    text = user_input.strip()

    # ---- 1. 检查疾病关键词 ----
    for keyword in DISEASE_KEYWORDS:
        if keyword in text:
            return {
                "level": "block",
                "trigger": "疾病",
                "keyword": keyword,
                "message": SAFETY_BLOCK_MESSAGE,
            }

    # ---- 2. 检查严重症状关键词 ----
    for keyword in SYMPTOM_KEYWORDS:
        if keyword in text:
            return {
                "level": "block",
                "trigger": "症状",
                "keyword": keyword,
                "message": SAFETY_BLOCK_MESSAGE,
            }

    # ---- 3. 检查特殊人群关键词 ----
    for keyword in SPECIAL_POPULATION_KEYWORDS:
        if keyword in text:
            return {
                "level": "block",
                "trigger": "特殊人群",
                "keyword": keyword,
                "message": SAFETY_BLOCK_MESSAGE,
            }

    # ---- 4. 年龄判断 ----
    age_triggered, age_desc = _check_age_issue(text)
    if age_triggered:
        return {
            "level": "block",
            "trigger": "特殊人群",
            "keyword": age_desc,
            "message": SAFETY_BLOCK_MESSAGE,
        }

    # ---- 5. 检查灰色地带关键词 ----
    for keyword, desc in GRAY_AREA_MAP.items():
        if keyword in text:
            warning = (
                f"*温馨提示：{desc}，建议先排除病理因素。"
                f"以下的建议仅供健康人群参考，若感到不适请立即停止。*"
            )
            return {
                "level": "warn",
                "keyword": keyword,
                "warning": warning,
            }

    # ---- 6. 安全 ----
    return {"level": "safe"}


# ============================================================
# ★ 批量检测（调试用）
# ============================================================
def diagnose_check(text: str) -> str:
    """
    调试用：对输入文本进行安全诊断，返回人类可读的诊断报告。
    """
    result = check_user_input(text)
    level = result["level"]

    if level == "block":
        return (
            f"[熔断] 触发类型: {result['trigger']}, "
            f"命中关键词: {result['keyword']}\n"
            f"回复: {result['message']}"
        )
    elif level == "warn":
        return (
            f"[警告] 命中灰色关键词: {result['keyword']}\n"
            f"警告前缀: {result['warning']}"
        )
    else:
        return "[安全] 未触发任何安全规则，正常处理"


